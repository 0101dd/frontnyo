          <h3>書譜原文</h3>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>夫自古之善書者，漢魏有鍾、張之絕，晉末稱二王之妙。王羲之云：「頃尋諸名書，鍾張信為絕倫，其餘不足觀。」可謂鍾、張云沒，而羲、獻繼之。又云：「吾書比之鍾張，鍾當抗行，或謂過之。張草猶當雁行。然張精熟，池水盡墨，假令寡人耽之若此，未必謝之。」此乃推張邁鍾之意也。考其專擅，雖未果於前規；摭以兼通，故無慚於即事。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>評者云：「彼之四賢，古今特絕；而今不逮古，古質而今研。」夫質以代興，妍因俗易。雖書契之作，適以記言；而淳醨一遷，質文三變，馳騖沿革，物理常然。貴能古不乖時，今不同弊，所謂「文質彬彬。然後君子。」何必易雕宮於穴處，反玉輅於椎輪者乎！又云：「子敬之不及逸少，猶逸少之不及鍾張。」意者以為評得其綱紀，而未詳其始卒也。且元常專工于隸書，伯英尤精於草體，彼之二美，而逸少兼之。擬草則餘真，比真則長草，雖專工小劣，而博涉多優。總其終始，匪無乖互。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>謝安素善尺牘，而輕子敬之書。子敬嘗作佳書與之，謂必存錄，安輒題後答之，甚以為恨。安嘗問敬：「卿書何如右軍？」」答云：「.故當勝。」安云：「物論殊不爾。」於敬又答：「時人那得知！」敬雖權以此辭折安所鑒，自稱勝父，不亦過乎！且立身揚名，事資尊顯，勝母之里，曾參不入。以於敬之豪翰，紹右軍之筆札，雖復粗傳楷則，實恐未克箕裘。況乃假託神仙，恥崇家範，以斯成學，孰愈面牆！後羲之往都，臨行題壁。子敬密拭除之，輒書易其處，私為不惡。羲之還見，乃歎曰：「吾去時真大醉也！」敬乃內慚。是知逸少之比鍾張，則專博斯別；子敬之不及逸少，無或疑焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>余志學之年，留心翰墨，味鍾張之餘烈，挹羲獻之前規，極慮專精，時逾二紀。有乖入木之術，無間臨池之志。觀夫懸針垂露之異，奔雷墜石之奇，鴻飛獸駭之資，鸞舞蛇驚之態，絕岸頹峰之勢，臨危據槁之形；或重若崩雲，或輕如蟬翼；導之則泉注，頓之則山安；纖纖乎似初月之出天涯，落落乎猶眾星之列河漢；同自然之妙有，非力運之能成；信可謂智巧兼優，心手雙暢，翰不虛動，下必有由。一畫之間，變起伏於鋒杪；一點之內，殊衄挫於毫芒。況云積其點畫，乃成其字；曾不傍窺尺犢，俯習寸陰；引班超以為辭，援項籍而自滿；任筆為體，聚墨成形；心昏擬效之方，手迷揮運之理，求其妍妙，不亦謬哉！</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>然君子立身，務修其本。楊雄謂：詩賦小道，壯夫不為。況複溺思毫釐，淪精翰墨者也！夫潛神對奕，猶標坐隱之名；樂志垂綸，尚體行藏之趣。詎若功宣禮樂，妙擬神仙，猶埏埴之罔窮，與工爐而並運。好異尚奇之士；玩體勢之多方；窮微測妙之夫，得推移之奧賾。著述者假其糟粕，藻鑒者挹其菁華，固義理之會歸，信賢達之兼善者矣。存精寓賞，豈徒然與？</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>而東晉士人，互相陶淬。至於王謝之族，郗庾之倫，縱不盡其神奇，咸亦挹其風味。去之滋永，斯道愈微。方復聞疑稱疑，得末行末，古今阻絕，無所質問；設有所會，緘秘已深；遂令學者茫然，莫知領要，徒見成功之美，不悟所致之由。或乃就分布于累年，向規矩而猶遠，圖真不悟，習草將迷。假令薄解草書，粗傳隸法，則好溺偏固，自閡通規。詎知心手會歸，若同源而異派；轉用之術，猶共樹而分條者乎？加以趨吏適時，行書為要；題勒方幅，真乃居先。草不兼真，殆於專謹；真不通草，殊非翰札，真以點畫為形質，使轉為情性；草以點畫為情性，使轉為形質。草乖使轉，不能成字；真虧點畫，猶可記文。回互雖殊，大體相涉。故亦傍通二篆，俯貫八分，包括篇章，涵泳飛自。若毫釐不察，則胡越殊風者焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>至如鍾繇隸奇，張芝草聖，此乃專精一體，以致絕倫。伯英不真，而點畫狼藉；元常不草，使轉縱橫。自茲已降，不能兼善者，有所不逮，非專精也。雖篆隸草章，工用多變，濟成厥美，各有攸宜。篆尚婉而通，隸欲精而密，草貴流而暢，章務檢而便。然後凜之以風神，溫之以妍潤，鼓之以枯勁，和之以閒雅。故可達其情性，形其哀樂，驗燥濕之殊節，千古依然。體老壯之異時，百齡俄頃。嗟呼！不入其門，詎窺其奧者也。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>又一時而書，有乖有合，合則流媚，乖則雕疏，略言其由，各有其五：神怡務閑，一合也；感惠徇知，二合也；時和氣潤，三合也；紙墨相發，四合也；偶然欲書，五合也。心遽體留，一乖也；意違勢屈，二乖也；風燥日炎，三乖也；紙墨不稱，四乖也；情怠手闌，五乖也。乖合之際，優劣互差。得時不如得器，得器不如得志，若五乖同萃，思遏手蒙；五合交臻，神融筆暢。暢無不適，蒙無所從。當仁者得意忘言，罕陳其要；企學者希風敘妙，雖述猶疏。徒立其工，未敷厥旨。不揆庸昧，輒效所明；庶欲弘既往之風規，導將來之器識，除繁去濫，睹跡明心者焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>代有《筆陣圖》七行，中畫執筆三手，圖貌乖舛，點畫湮訛。頃見南北流傳，疑是右軍所制。雖則未詳真偽，尚可發啟童蒙。既常俗所存，不藉編錄。至於諸家勢評，多涉浮華，莫不外狀其形，內迷其理，今之所撰，亦無取焉。若乃師宜官之高名，徒彰史牒；邯鄲淳之令範，空著縑緗。暨乎崔、杜以來，蕭、羊已往，代祀綿遠，名氏滋繁。或藉甚不渝，人亡業顯；或憑附增價，身謝道衰。加以糜蠹不傳，搜秘將盡，偶逢緘賞，時亦罕窺，優劣紛紜，殆難覼縷。其有顯聞當代，遺跡見存，無俟抑揚，自標先後。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>且六文之作，肇自軒轅；八體之興，始於嬴正。其來尚矣，厥用斯弘。但今古不同，妍質懸隔，既非所習，又亦略諸。複有龍蛇雲露之流，龜鶴花英之類，乍圖真於率爾，或寫瑞于當年，巧涉丹青，工虧翰墨，異夫楷式，非所詳焉。代傳羲之與子敬筆勢論十章，文鄙理疏，意乖言拙，詳其旨趣，殊非右軍。且右軍位重才高，調清詞雅，聲塵未泯，翰櫝仍存。觀夫致一書，陳一事，造次之際，稽古斯在；豈有貽謀令嗣，道叶義方，章則頓虧，一至於此！又云與張伯英同學，斯乃更彰虛誕。若指漢末伯英，時代全不相接；必有晉人同號，史傳何其寂寥！非訓非經，宜從棄擇。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>夫心之所達，不易盡于名言；言之所通，尚難形於紙墨。粗可髣髴其狀，綱紀其辭。冀酌希夷，取會佳境。闕而末逮，請俟將來。今撰執使轉用之由，以袪未悟。執謂深淺長短之類是也；使謂縱橫牽掣之類是也；轉謂鉤環盤紆之類是也；用謂點畫向背之類是也。方復會其數法，歸於一途；編列眾工，錯綜群妙，舉前人之未及，啟後學於成規，窮其根源，析其枝派。貴使文約理贍，跡顯心通；披卷可明，下筆無滯。詭辭異說，非所詳焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>然今之所陳，務裨學者。但右軍之書，代多稱習，良可據為宗匠，取立指歸。豈惟會古通今，亦乃情深調合。致使摹搨日廣，研習歲滋，先後著名，多從散落；歷代孤紹，非其効與？試言其由，略陳數意：止如《樂毅論》《黃庭經》《東方朔畫贊》《太史箴》《蘭亭集序》《告誓文》，斯並代俗所傳，真行絕致者也。寫《樂毅》則情多佛鬱；書《畫贊》則意涉瑰奇；《黃庭經》則怡懌虛無；《太史箴》又縱橫爭折；暨乎《蘭亭》興集，思逸神超，私門誡誓，情拘志慘。所謂涉樂方笑，言哀已歎。豈惟駐想流波，將貽嘽噯之奏；馳神睢渙，方思藻繪之文。雖其目擊道存，尚或心迷議舛。莫不強名為體，共習分區。豈知情動形言，取會風騷之意；陽舒陰慘，本乎天地之心。既失其情，理乖其實，原夫所致，安有體哉！</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>夫運用之方，雖由己出，規模所設，信屬目前，差之一豪，失之千里，苟知其術，適可兼通。心不厭精，手不忘熟。若運用盡於精熟，規矩諳于胸襟，自然容與徘徊，意先筆後，瀟灑流落，翰逸神飛，亦猶弘羊之心，預乎無際；庖丁之目，不見全牛。嘗有好事，就吾求習，吾乃粗舉綱要，隨而授之，無不心悟手從，言忘意得，縱未窮於眾術，斷可極於所詣矣。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>若思通楷則，少不如老；學成規矩，老不如少。思則老而愈妙，學乃少而可勉。勉之不已，抑有三時；時然一變，極其分矣。至如初學分佈，但求平正；既知平正，務追險絕，既能險絕，復歸平正。初謂未及，中則過之，後乃通會，通會之際，人書俱老。仲尼云：五十知命，七十從心。故以達夷險之情，體權變之道，亦猶謀而後動，動不失宜；時然後言，言必中理矣。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>是以右軍之書，末年多妙，當緣思慮通審，志氣和平，不激不厲，而風規自遠。子敬已下，莫不鼓努為力，標置成體，豈獨工用不侔，亦乃神情懸隔者也。或有鄙其所作，或乃矜其所運。自矜者將窮性域，絕於誘進之途；自鄙者尚屈情涯，必有可通之理。嗟乎，蓋有學而不能，未有不學而能者也。考之即事，斷可明焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>然消息多方，性情不一，乍剛柔以合體，忽勞逸而分驅。或恬憺雍容，內涵筋骨；或折挫槎枿，外曜鋒芒。察之者尚精，擬之者貴似。況擬不能似，察不能精，分佈猶疏，形骸未撿；躍泉之態，未睹其妍，窺井之談，已聞其醜。縱欲唐突羲獻，誣罔鍾張，安能掩當年之目，杜將來之口！慕習之輩，尤宜慎諸。至有未悟淹留，偏追勁疾；不能迅速，翻效遲重。夫勁速者，超逸之機，遲留者，賞會之致。將反其速，行臻會美之方；專溺于遲，終爽絕倫之妙。能速不速，所謂淹留；因遲就遲，詎名賞會！非其心閑手敏，難以兼通者焉。</p>
            </div>
          </div>
          <div class="row">
            <div class="mx-auto col-sm-12 col-md-10 col-lg-6 col-12">
              <p>假令眾妙攸歸，務存骨氣；骨既存矣，而遒潤加之。亦猶枝幹扶疏，淩霜雪而彌勁；花葉鮮茂，與雲日而相暉。如其骨力偏多，遒麗蓋少，則若枯槎架險，巨石當路，雖妍媚云闕，而體質存焉。若遒麗居優，骨氣將劣，譬夫芳林落蕊，空照灼而無依；蘭沼漂萍，徒青翠而奚托。是知偏工易就，盡善難求。雖學宗一家，而變成多體，莫不隨其性欲，便以為姿：質直者則徑侹不遒；剛佷者又倔強無潤；矜斂者弊於拘束；脫易者失於規矩；溫柔者傷於軟緩，躁勇者過於剽迫；狐疑者溺於滯澀；遲重者終於蹇鈍；輕瑣者淬於俗吏。斯皆獨行之士，偏玩所乖。</p>
